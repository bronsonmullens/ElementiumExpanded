package com.bronsonmullens.elementiumexpanded.util;

import com.bronsonmullens.elementiumexpanded.ElementiumExpanded;
import com.bronsonmullens.elementiumexpanded.items.ItemBase;
import net.minecraft.item.Item;
import net.minecraftforge.fml.RegistryObject;
import net.minecraftforge.fml.javafmlmod.FMLJavaModLoadingContext;
import net.minecraftforge.registries.DeferredRegister;
import net.minecraftforge.registries.ForgeRegistries;

public class RegistryHandler {

    public static final DeferredRegister<Item> ITEMS = new DeferredRegister<>(ForgeRegistries.ITEMS, ElementiumExpanded.MOD_ID);

    public static void init() {
        ITEMS.register(FMLJavaModLoadingContext.get().getModEventBus());
    }

    // Items
    public static final RegistryObject<Item> ELEMENTIUM = ITEMS.register("elementium", ItemBase::new);

}
